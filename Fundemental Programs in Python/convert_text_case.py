#Python program to convert all characters

def chracter(s):
    txt = str(s)
    print("Uppercase: ",txt.upper())
    print('------------------------------')
    print("Lowercase: ",txt.lower())
    print('------------------------------')
    print("Capitalize: ",txt.capitalize())
    print('------------------------------')
    print("Title: ",txt.title())
    print('------------------------------')


chracter('python program')
        
